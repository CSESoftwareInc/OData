        /// <summary>
        /// Link to previous page of results
        /// </summary>
        public const string PreviousPage = "prev";

        /// <summary>
        /// Link to first page of results
        /// </summary>
        public const string FirstPage = "fisrt";

        /// <summary>
        /// Link to last page of results
        /// </summary>
        public const string LastPage = "last";
